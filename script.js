function applyStaticTranslations() {
  document.documentElement.lang = state.lang;
  document.title = translations[state.lang].meta.title;
  document.querySelector('meta[name="description"]').setAttribute("content", translations[state.lang].meta.description);
  document.querySelector('meta[property="og:title"]').setAttribute("content", translations[state.lang].meta.ogTitle);
  document.querySelector('meta[property="og:description"]').setAttribute("content", translations[state.lang].meta.ogDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getTranslation(element.dataset.i18n);
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = getTranslation(element.dataset.i18nAlt);
    if (value) element.setAttribute("alt", value);
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const value = getTranslation(element.dataset.i18nTitle);
    if (value) element.setAttribute("title", value);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = getTranslation(element.dataset.i18nPlaceholder);
    if (value) element.setAttribute("placeholder", value);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    const value = getTranslation(key);
    if (value) element.setAttribute("aria-label", value);
  });

  const toggle = document.querySelector("[data-menu-toggle]");
  if (toggle) {
    const labelKey = toggle.getAttribute("aria-expanded") === "true" ? "aria.closeMenu" : "aria.openMenu";
    toggle.setAttribute("aria-label", getTranslation(labelKey));
  }
}

