function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem("autohouse-lang", lang);
  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    const isActive = button.dataset.langSwitch === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
  applyStaticTranslations();
  renderDynamicContent();
}

function closeMenu() {
  document.body.classList.remove("menu-open");
  const toggle = document.querySelector("[data-menu-toggle]");
  if (toggle) {
    toggle.setAttribute("aria-expanded", "false");
    toggle.setAttribute("aria-label", getTranslation("aria.openMenu"));
  }
}

function bindMenu() {
  const toggle = document.querySelector("[data-menu-toggle]");
  toggle.addEventListener("click", () => {
    const nextState = toggle.getAttribute("aria-expanded") !== "true";
    document.body.classList.toggle("menu-open", nextState);
    toggle.setAttribute("aria-expanded", String(nextState));
    toggle.setAttribute("aria-label", getTranslation(nextState ? "aria.closeMenu" : "aria.openMenu"));
  });

  document.addEventListener("click", (event) => {
    const target = event.target;
    if (!(target instanceof Element)) return;
    if (target.closest("[data-menu-toggle]") || target.closest(".site-nav")) return;
    closeMenu();
  });
}

function bindLanguageSwitch() {
  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.langSwitch));
  });
}

function bindAnchors() {
  document.addEventListener("click", (event) => {
    const link = event.target.closest('a[href^="#"]');
    if (!link) return;
    const target = document.querySelector(link.getAttribute("href"));
    if (!target) return;
    closeMenu();
    event.preventDefault();
    target.scrollIntoView({ behavior: "smooth", block: "start" });
    if (link.hasAttribute("data-book-link")) {
      window.setTimeout(() => document.getElementById("name")?.focus({ preventScroll: true }), 450);
    }
  });
}

function bindBackToTop() {
  const button = document.querySelector("[data-back-to-top]");
  window.addEventListener("scroll", () => {
    button.classList.toggle("is-visible", window.scrollY > 700);
  });
  button.addEventListener("click", () => window.scrollTo({ top: 0, behavior: "smooth" }));
}

function bindActiveNav() {
  const links = Array.from(document.querySelectorAll("[data-nav-link]"));
  const sections = links
    .map((link) => document.querySelector(link.getAttribute("href")))
    .filter(Boolean);

  const observer = new IntersectionObserver(
    (entries) => {
      const visible = entries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (!visible) return;
      links.forEach((link) => {
        link.classList.toggle("is-active", link.getAttribute("href") === `#${visible.target.id}`);
      });
    },
    { rootMargin: "-30% 0px -55% 0px", threshold: [0.1, 0.35, 0.6] }
  );

  sections.forEach((section) => observer.observe(section));
}

function bindRevealAnimations() {
  const observed = document.querySelectorAll(".section-observed");
  if (!("IntersectionObserver" in window)) {
    observed.forEach((section) => section.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.12 }
  );
  observed.forEach((section) => {
    section.classList.add("reveal-ready");
    observer.observe(section);
  });
}

function bindForm() {
  const form = document.getElementById("request-form");
  const nameInput = document.getElementById("name");
  const phoneInput = document.getElementById("phone");
  const status = document.querySelector("[data-form-status]");
  const phonePattern = /^[+\d\s()-]{5,}$/;

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    status.textContent = "";
    document.querySelectorAll("[data-error-for]").forEach((error) => {
      error.textContent = "";
    });
    [nameInput, phoneInput].forEach((input) => input.removeAttribute("aria-invalid"));

    let isValid = true;
    if (!nameInput.value.trim()) {
      isValid = false;
      nameInput.setAttribute("aria-invalid", "true");
      document.querySelector('[data-error-for="name"]').textContent = translations[state.lang].form.nameError;
    }
    if (!phonePattern.test(phoneInput.value.trim())) {
      isValid = false;
      phoneInput.setAttribute("aria-invalid", "true");
      document.querySelector('[data-error-for="phone"]').textContent = translations[state.lang].form.phoneError;
    }
    if (!isValid) return;

    // Connect this point to email, Telegram, Viber, or a backend API when production delivery is ready.
    status.textContent = translations[state.lang].form.success;
    form.reset();
    renderServiceOptions();
  });
}

